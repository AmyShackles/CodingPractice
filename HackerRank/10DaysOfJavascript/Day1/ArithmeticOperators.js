/* 
- Write a function, getArea(length, width) which returns the area.
- Write a function, getPerimeter(length, width) which returns the perimeter.

*/

function getArea(length, width) {
  let area;
  area = length * width;
  return area;
}

function getPerimeter(length, width) {
  let perimeter;
  perimeter = 2 * (length + width);
  return perimeter;
}
